
function img_changer1()
{
	var a = document.getElementById('novo');
	var b = document.getElementById('foot');
	if (a.style.display == "none") 
		{
			a.style.display = "block";	
			b.style.display = "none";
		}
	else
		{
			b.style.display = "block";
			a.style.display = "none";
		}

}
